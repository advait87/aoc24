func checkHorizontal(stringArray: [Character]) -> Int {
    var hs = 0
    var ih = 0
    while ih < stringArray.count - 3 {
        if String(stringArray[ih]) == "X" && String(stringArray[ih+1]) == "M" && String(stringArray[ih+2]) == "A" && String(stringArray[ih+3]) == "S" {
            hs += 1
        }

        ih += 1
    }
    return hs
}

func checkReverseHorizontal(stringArray: [Character]) -> Int {
    var hs = 0
    var ih = 0
    while ih < stringArray.count - 3 {
        if String(stringArray[ih]) == "S" && String(stringArray[ih+1]) == "A" && String(stringArray[ih+2]) == "M" && String(stringArray[ih+3]) == "X" {
            hs += 1
        }

        ih += 1
    }
    return hs
}

func checkVertical(stringArray: [Character], lineLength: Int) -> Int {
    var vs = 0
    var iv = 0
    while (iv < (stringArray.count - (lineLength * 3 + 1))) {
        if stringArray[iv] == "X" && stringArray[iv + lineLength] == "M" && stringArray[iv + (2*lineLength)] == "A" && stringArray[iv + (3*lineLength)] == "S"{
            vs += 1
        }
        iv += 1
    }
    return vs
}

